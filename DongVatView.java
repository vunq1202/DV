    void loadData(ArrayList<DongVat> list) {
        defaultTableModel = (DefaultTableModel) tblDV.getModel();
        defaultTableModel.setRowCount(0);
        for (DongVat dongVat : list) {
            defaultTableModel.addRow(new Object[]{
                dongVat.getMaID(),
                dongVat.getTen(),
                dongVat.getLoai(),
                dongVat.getGioiTinh()

            });
        }
    }
