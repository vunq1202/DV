    private void btnThemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnThemMouseClicked
        // TODO add your handling code here:
        //lay thong tin tren form
        String maLoai = txtID.getText();
        String ten = txtTen.getText();
        String loai = cboLoai.getSelectedItem().toString();
        String gioiTinh = "";
        if (rdDuc.isSelected()) {
            gioiTinh = "duc";
        } else {
            gioiTinh = "cai";
        }
        if (ten.isEmpty()) {
            JOptionPane.showMessageDialog(this, "khong duoc bo trong");
        }else{
         //tao doi tuong tu cac thong tin treen
        DongVat dv = new DongVat(maLoai, ten, loai, gioiTinh);
        //them vao list
        Boolean checkData = qldv.addNew(dv);
        if (checkData) {
            JOptionPane.showMessageDialog(this, "thanh cong");
            loadData(qldv.getlstDV());
        }
        }
        
    }//GEN-LAST:event_btnThemMouseClicked

