    private void btnCapNhatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCapNhatMouseClicked
        // TODO add your handling code here:
        int i = tblDV.getSelectedRow();
        String maLoai = txtID.getText();
        String ten = txtTen.getText();
        String loai = cboLoai.getSelectedItem().toString();
        String gioiTinh = "";
        if (rdDuc.isSelected()) {
            gioiTinh = "duc";
        } else {
            gioiTinh = "cai";
        }
        //tao doi tuong tu cac thong tin treen
        DongVat dv = new DongVat(maLoai, ten, loai, gioiTinh);
        //them vao list
        Boolean checkData = qldv.update(i, dv);
        if (checkData) {
            JOptionPane.showMessageDialog(this, "them thanh cong");
            loadData(qldv.getlstDV());
        }

    }//GEN-LAST:event_btnCapNhatMouseClicked

